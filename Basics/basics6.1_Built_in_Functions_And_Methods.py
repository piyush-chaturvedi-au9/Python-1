# For strings
'''String Methods'''
# Captialize()
name = "praveen"
print("capitalize()", name.capitalize())  # this will captailize only the first letter

name_upper = name.upper()  # This will convert all lower case to upper
print("upper() ", name_upper)
name_lower = name_upper.lower()  # this will lower all upper case # casefold()
print("CaseFold() ", name.casefold())
print("casefold()", name_lower)

