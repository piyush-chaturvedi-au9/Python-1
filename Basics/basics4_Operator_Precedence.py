# Operator Precedence
"""
In our Mathematics BODMAS was the rule
where ,
B - Bracket
O - Orders
D - Division
M - Multiplication
A - Addition
S - Subtraction
"""
"""
Same like this in CS or in Programming we have BIDMAS
B - Bracket
I - Indices (power values or ex : 2**2)
D - Division
M - Multiplication
A - Addition
S - Subtraction
"""
'''
Solve in mind first and check answers later 
'''
print((5 + 4) * 10 / 2)
print(((5 + 4) * 10) / 2)
print((5 + 4) * (10 / 2))
print(5 + (4 * 10) / 2)
print(5 + 4 * 10 // 2)
